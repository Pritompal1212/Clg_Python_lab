# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)           # Output: 13
print("Subtraction:", a - b)        # Output: 7
print("Multiplication:", a * b)     # Output: 30
print("Division:", a / b)           # Output: 3.3333...
print("Modulus:", a % b)            # Output: 1
print("Exponentiation:", a ** b)    # Output: 1000
print("Floor Division:", a // b)    # Output: 3

print("===========================1===========================")

# Relational Operators
x = 10
y = 20

print("x > y:", x > y)   # Output: False
print("x < y:", x < y)   # Output: True
print("x >= y:", x >= y) # Output: False
print("x <= y:", x <= y) # Output: True
print("x == y:", x == y) # Output: False
print("x != y:", x != y) # Output: True

print("===========================2===========================")

# Assignment Operators
x = 5
print("Initial x:", x)  # Output: 5

x += 3
print("After x += 3:", x)  # Output: 8

x *= 2
print("After x *= 2:", x)  # Output: 16

x %= 5
print("After x %= 5:", x)  # Output: 1

print("===========================4===========================")

# Logical Operators
a = True
b = False

print("a and b:", a and b)  # Output: False
print("a or b:", a or b)    # Output: True
print("not a:", not a)      # Output: False

print("===========================3===========================")

# Bitwise Operators
x = 5  # Binary: 0101
y = 3  # Binary: 0011

print("x & y (AND):", x & y)   # Output: 1 (Binary: 0001)
print("x | y (OR):", x | y)    # Output: 7 (Binary: 0111)
print("x ^ y (XOR):", x ^ y)   # Output: 6 (Binary: 0110)
print("~x (NOT):", ~x)         # Output: -6
print("x << 1 (Left Shift):", x << 1)  # Output: 10
print("x >> 1 (Right Shift):", x >> 1) # Output: 2

print("===========================5===========================")

