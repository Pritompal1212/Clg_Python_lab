#to create ,display,append,insert and reverse the order of the items in the array
# Step 1: Create an array
my_array = []  # Initialize an empty array

# Step 2: Display the array
print("Initial Array:", my_array)

# Step 3: Append items to the array
my_array.append(10)
my_array.append(20)
my_array.append(30)
print("Array after appending items:", my_array)

# Step 4: Insert an item at a specific position
my_array.insert(1, 15)  # Insert 15 at index 1
print("Array after inserting 15 at index 1:", my_array)

# Step 5: Reverse the order of the items
my_array.reverse()
print("Array after reversing the order:", my_array)
